main() {
    echo "Installing Haystack Offline Module..."
    installModule
    echo "... Done"
}

#------------------------------------------------------------------------------------------
# INTERNAL FUNCTIONS
#------------------------------------------------------------------------------------------
function installModule()
{
    chmod u+x DecryptFiles.sh
    chmod u+x SetupClient.sh
    brew install openssl > removeThis
    rm removeThis
    gcc offline.c -o HaystackOffline
}

#------------------------------------------------------------------------------------------
# SCRIPT
#------------------------------------------------------------------------------------------
    main
